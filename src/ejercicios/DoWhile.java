package ejercicios;

public class DoWhile {

    public static void main(String[] args) {
        // Para el bucle Do While, deber√°s crear la misma estructura que en el While, pero solo se debe ejecutar una vez.
        int numeroWhile = 4;

        do {
            System.out.println(numeroWhile);
            numeroWhile++;
        } while (numeroWhile < 3);

    }
}
